package vectorone;

public class VectorOne {

    public static void main(String[] args) {
        int n[] = {3, 2, 8, 7, 5, 4};
        
        System.out.println("Length of vector is: "+n.length+" positions");
        for (int c = 0; c <= n.length-1; c++){
            System.out.println(n[c]);
        }
    }

}
